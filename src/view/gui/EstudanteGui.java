    private void lerTabela(){
        //Objeto DAO
         estudanteDao = new EstudanteDao();
         
         //Limpando a tabela
         modelo.clearTable();
        
        for(Estudante e: estudanteDao.read())
            modelo.addRow(e);
        
    }
    
