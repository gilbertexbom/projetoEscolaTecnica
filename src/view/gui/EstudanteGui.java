    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        int linha = tblEstudante.getSelectedRow();
        
        if(linha != -1){
            estudanteDao = new EstudanteDao();
            estudanteDao.update(new Estudante(
                    txtRm.getText(),
                    txtNome.getText()),
                    //Usa-se o RM da linha selecionada como parâmetro
                    String.valueOf(modelo.getValueAt(linha, 0))
            );
            
            /*
            modelo.setValueAt(txtRm.getText(), linha, 0);
            modelo.setValueAt(txtNome.getText(), linha, 1);
            */
        }
        this.btnLimparActionPerformed(evt);
        this.lerTabela();
        
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void tblEstudanteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEstudanteMouseClicked
        // TODO add your handling code here:
        int linha = tblEstudante.getSelectedRow();
        
        if(linha != -1){
            txtRm.setText(String.valueOf(modelo.getValueAt(linha, 0)));
            txtNome.setText(String.valueOf(modelo.getValueAt(linha, 1)));
        }
        
    }//GEN-LAST:event_tblEstudanteMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linha = tblEstudante.getSelectedRow();
        
        if(linha != -1){
            estudanteDao = new EstudanteDao();
            estudanteDao.delete(new Estudante(
                    txtRm.getText(),
                    txtNome.getText())
            );
            //modelo.removeRow(linha);
        }
        this.btnLimparActionPerformed(evt);
        this.lerTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed

